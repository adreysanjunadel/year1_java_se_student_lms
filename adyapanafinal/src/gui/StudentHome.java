    private void insertAttendance(String classCode) {
        try {
            session = model.StudentSession.getInstance();
            String sno = session.getSno(); // Declare and initialize the sno variable

            // Check if the attendance for the given sno and classno already exists
            String checkAttendanceQuery = "SELECT * FROM `attendance` JOIN `student_has_class` ON "
                    + "`attendance`.`student_sno` = `student_has_class` WHERE `student_sno` = ? AND `class_classno` = ?";
            ResultSet resultSet = MySQL.executePreparedStatement(checkAttendanceQuery, sno, classCode);

            if (resultSet.next()) {
                // Attendance already exists
                JOptionPane.showMessageDialog(this, "Attendance already marked for this class.", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                // Attendance does not exist, insert the data into the attendance table
                String attendanceQuery = "INSERT INTO `attendance` (`student_sno`, `class_classno`) VALUES (?, ?)";
                MySQL.executePreparedStatement(attendanceQuery, sno, classCode);

                // Display a success message or perform any other necessary actions
                JOptionPane.showMessageDialog(this, "Attendance marked successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

