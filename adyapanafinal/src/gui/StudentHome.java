    private void loadMySubjects() {
        try {
            String sno = session.getSno();
            ResultSet resultSet = MySQL.execute("SELECT * FROM `subject` "
                    + "INNER JOIN `invoice` ON `invoice`.`subno` = `subject`.`subno` "
                    + "INNER JOIN `student` ON `invoice`.`sno` = `student`.`sno` "
                    + "WHERE `student`.`sno` = '" + sno + "' ");

            Vector<String> v = new Vector<>();
            v.add("Select");

            while (resultSet.next()) {
                String description = resultSet.getString("description");
                v.add(description);
                int subno = resultSet.getInt("subno");
                subjectMap.put(description, subno);
            }

            if (v.size() > 1) {
                jLabel7.setText(v.get(1));
            }
            if (v.size() > 2) {
                jLabel8.setText(v.get(2));
            }
            if (v.size() > 3) {
                jLabel9.setText(v.get(3));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadClassSchedule() {
        try {
            ResultSet resultSet = MySQL.execute("SELECT `class`.`classno`, `teacher`.`first_name`, `teacher`.`last_name`, `subject`.`description`, `class`.`date`, `class`.`start_time`, `class`.`end_time` "
                    + "FROM `class` "
                    + "JOIN `subject` ON `class`.`subno` = `subject`.`subno` "
                    + "JOIN `teacher` ON `class`.`tno` = `teacher`.`tno` "
                    + "JOIN `student_has_class` ON `class`.`classno` = `student_has_class`.`class_classno`"
                    + "WHERE `class`.`date` >= CURDATE() AND `class`.`start_time` >= CURTIME() "
                    + "ORDER BY `class`.`date` ASC, `class`.`start_time` ASC");

            // Initialize the table model if it's null
            if (tableModel == null) {
                tableModel = new DefaultTableModel(
                        new Object[][]{},
                        new String[]{"Class Code", "Teacher", "Subject", "Date", "Start Time", "End Time"}
                );
            }

            tableModel.setRowCount(0);

            while (resultSet.next()) {
                String classCode = resultSet.getString("classno");
                String firstName = resultSet.getString("first_name");
                String lastName = resultSet.getString("last_name");
                String teacherName = firstName + " " + lastName;
                String subjectDescription = resultSet.getString("description");
                String date = resultSet.getString("date");
                String startTime = resultSet.getString("start_time");
                String endTime = resultSet.getString("end_time");

                tableModel.addRow(new Object[]{classCode, teacherName, subjectDescription, date, startTime, endTime});
            }

            // Set the table model to jTable1
            jTable1.setModel(tableModel);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

